package chap3;

public class java3_1 {

	public static void main(String[] args) {
		// 반복문
		// for문
		for (int i = 0; i < 3; i++ )
		{
			System.out.println("java programming");
		}
		
		// while문
		int i = 0;
		while (i < 3)
		{
			System.out.println("java programming");
			i++;
		}
		
		// do~while문
		do
		{
			System.out.println("java programming");
		} while (false);
	}	

}
